
percent_float = float(input("What is your percentage? "))

if 90 <= percent_float < 100:
    print("you got an A")
if 80 <= percent_float < 90:
    print("you got a B")
if 70 <= percent_float < 80:
    print("you got a C")
if 60 <= percent_float < 70:
    print("you received a D")
else:
    print("oops. not good.")
